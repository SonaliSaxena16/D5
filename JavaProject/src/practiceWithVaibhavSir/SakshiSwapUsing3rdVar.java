package practiceWithVaibhavSir;

public class SakshiSwapUsing3rdVar {

	public static void main(String[] args) {

		int swap=0, a=10, b=20;
		
		// output a=20 b=10
		
		
		swap=a; // swap=10 a=0
		a=b;    // a=20 b=0
		b=swap;
		
		System.out.println("a val " + a);

		System.out.println("b val " + b);

		
		
	}

}
